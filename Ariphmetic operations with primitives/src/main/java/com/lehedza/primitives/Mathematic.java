package main.java.com.lehedza.primitives;


public class Mathematic {

    public double getSumIntDouble(int a, double b) {
        return a + b;
    }

    public double getResultOfSum(double firstNum, double secondNum) {
        double res = firstNum + secondNum;
        return res;
    }

    public double getResultOfIncrease(double firstNum, double secondNum) {
        double res = firstNum * secondNum;
        return res;
    }

    public double getResultOfDivide(double firstNum, double secondNum) {
        double res = firstNum / secondNum;
        return res;
    }

    public double getResOfSusbstract(double firstNum, double secondNum) {
        double res = firstNum - secondNum;
        return res;
    }

}

